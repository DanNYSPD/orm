public class Salone{
	private int idSalon;
	private int numSalon;
	private int claveSalon;

	public int getIdSalon(){
		return this.idSalon;
	}
	public void setIdSalon(int idSalon){
		this.idSalon=idSalon;
	}
	public int getNumSalon(){
		return this.numSalon;
	}
	public void setNumSalon(int numSalon){
		this.numSalon=numSalon;
	}
	public int getClaveSalon(){
		return this.claveSalon;
	}
	public void setClaveSalon(int claveSalon){
		this.claveSalon=claveSalon;
	}
}